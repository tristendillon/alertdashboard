import { Sidebar as SidebarType } from "@workspace/ui/lib/types"
type SidebarTransitionProps = {
  children: React.ReactNode
  hasParent: boolean
  className?: string
  sidebars: SidebarType[]
}

const SidebarTransition = ({ children, hasParent, className, sidebars }: SidebarTransitionProps) => {
  const pathname = usePathname();
  const { open } = useSidebar();

  const parentVariant = {
    hidden: { opacity: 0, x: 50 },
    visible: { opacity: 1, x: 0, transition: { duration: 0.3 } },
    exit: { opacity: 0, x: 50, transition: { duration: 0.2 } },
  };

  const childVariant = {
    hidden: { opacity: 0, x: -50 },
    visible: { opacity: 1, x: 0, transition: { duration: 0.3 } },
    exit: { opacity: 0, x: -50, transition: { duration: 0.2 } },
  };

  const variants = hasParent && open ? parentVariant : childVariant;
  const currentSidebar = React.useMemo(() => sidebars.find(sidebar => sidebar.pathnames.includes(pathname)), [pathname, sidebars]);
  return (
    <AnimatePresence mode="wait">
      <motion.div
        className={cn(className)}
        key={currentSidebar?.key ?? "no-sidebar"}
        variants={variants}
        initial="hidden"
        animate="visible"
        exit="exit"
      >
        {children}
      </motion.div>
    </AnimatePresence>
  );
};


