const SIDEBAR_COOKIE_NAME = "sidebar_state"
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7
const SIDEBAR_WIDTH = "16rem"
const SIDEBAR_WIDTH_MOBILE = "18rem"
const SIDEBAR_WIDTH_ICON = "3rem"
const SIDEBAR_KEYBOARD_SHORTCUT = "b"
const SIDEBAR_ANIMATION_DURATION = 200

type SidebarContextProps = {
  state: "expanded" | "collapsed"
  open: boolean
  setOpen: (open: boolean) => void
  openMobile: boolean
  setOpenMobile: (open: boolean) => void
  isMobile: boolean
  toggleSidebar: () => void
}

const SidebarContext = React.createContext<SidebarContextProps | null>(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }

  return context
}

function getSidebarCookie(): boolean {
  const match = document.cookie.match(new RegExp('(^| )' + SIDEBAR_COOKIE_NAME + '=([^;]+)'))
  if (match) {
    return match[2] === 'true'
  }
  return true // default to open if no cookie is found
}

const SidebarProvider = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    defaultOpen?: boolean
    open?: boolean
    onOpenChange?: (open: boolean) => void
  }
>(
  (
    {
      defaultOpen = true,
      open: openProp,
      onOpenChange: setOpenProp,
      className,
      style,
      children,
      ...props
    },
    ref
  ) => {
    const isMobile = useIsMobile()
    const [openMobile, setOpenMobile] = React.useState(false)
    // TODO: This is a hack to prevent the sidebar from being rendered before the cookie is set.
    // We should find a better way to handle this.
    // Probably
    // 4/29/2025
    const [hasMounted, setHasMounted] = React.useState(false)

    React.useEffect(() => {
      setHasMounted(true)
    }, [])

    const [_open, _setOpen] = React.useState(() => {
      if (typeof window !== 'undefined') {
        return getSidebarCookie()
      }
      return defaultOpen
    })
    const [animatedClosed, setAnimatedClosed] = React.useState(!_open)

    // This is a way to do it aswell but it leaves a gap between the defaultOpen and the cookie being loaded.
    // React.useEffect(() => {
    //   if (openProp === undefined) {
    //     const cookieValue = getSidebarCookie()
    //     _setOpen(cookieValue)
    //   }
    // }, [openProp])

    const open = openProp ?? _open

    const setOpen = React.useCallback(
      (value: boolean | ((value: boolean) => boolean)) => {
        const openState = typeof value === "function" ? value(open) : value
        if (setOpenProp) {
          setOpenProp(openState)
        } else {
          _setOpen(openState)
        }

        // This sets the cookie to keep the sidebar state.
        document.cookie = `${SIDEBAR_COOKIE_NAME}=${openState}; path=/; max-age=${SIDEBAR_COOKIE_MAX_AGE}`
      },
      [setOpenProp, open]
    )

    // Helper to toggle the sidebar.
    const toggleSidebar = React.useCallback(() => {
      const nextOpen = !(isMobile ? openMobile : open)


      if (!nextOpen) {
        // We're closing the sidebar — delay the animatedClosed update
        setTimeout(() => {
          setAnimatedClosed(true)
        }, SIDEBAR_ANIMATION_DURATION)
      } else {
        // We're opening the sidebar — update immediately
        setAnimatedClosed(false)
      }

      if (isMobile) {
        setOpenMobile(nextOpen)
      } else {
        setOpen(nextOpen)
      }
    }, [isMobile, open, openMobile, setOpen, setOpenMobile])

    // Adds a keyboard shortcut to toggle the sidebar.
    React.useEffect(() => {
      const handleKeyDown = (event: KeyboardEvent) => {
        if (
          event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
          (event.metaKey || event.ctrlKey)
        ) {
          event.preventDefault()
          toggleSidebar()
        }
      }

      window.addEventListener("keydown", handleKeyDown)
      return () => window.removeEventListener("keydown", handleKeyDown)
    }, [toggleSidebar])

    // We add a state so that we can do data-state="expanded" or "collapsed".
    // This makes it easier to style the sidebar with Tailwind classes.
    const state = open ? "expanded" : "collapsed"

    const contextValue = React.useMemo<SidebarContextProps>(
      () => ({
        state,
        open,
        setOpen,
        isMobile,
        openMobile,
        setOpenMobile,
        toggleSidebar,
      }),
      [state, open, setOpen, isMobile, openMobile, setOpenMobile, toggleSidebar]
    )
    if (!hasMounted) return null
    return (
      <SidebarContext.Provider value={contextValue}>
        <AnimatePresence>
          <TooltipProvider delayDuration={0}>
            <div
              className={cn(
                "group/sidebar-wrapper flex w-full has-[[data-variant=inset]]:bg-sidebar",
                className
              )}
              ref={ref}
              {...props}
              data-animated-closed={animatedClosed}
            >
              {children}
            </div>
          </TooltipProvider>
        </AnimatePresence>
      </SidebarContext.Provider>
    )
  }
)
SidebarProvider.displayName = "SidebarProvider"

